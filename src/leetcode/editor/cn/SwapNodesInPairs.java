        public ListNode swapPairs(ListNode head) {
            ListNode prev = new ListNode();
            prev.next = head;
            ListNode tmp = prev;
            while (prev.next != null && prev.next.next != null) {
                ListNode start = prev.next;
                ListNode end = prev.next.next;
                prev.next = end;
                start.next = end.next;
                end.next = start;
                prev = start;
            }
            return tmp.next;
        }
